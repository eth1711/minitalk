#include "libft.h"

void	ft_printstr(char *s, int *count)
{
	int	i;

	i = 0;
	if (!s)
	{
		*count += write(1, "(null)", 6);
		return ;
	}
	while (s[i] != '\0')
	{
		*count += write(1, &s[i], 1);
		i++;
	}
}